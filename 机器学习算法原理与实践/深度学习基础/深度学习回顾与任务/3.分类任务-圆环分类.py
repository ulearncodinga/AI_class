def normal(bias):
    return init.normal_(bias, mean=0, std=0.01)


# 2.定义前向模型
class Model(nn.Module):
    def __init__(self):
        super(Model, self).__init__()
        self.layer1 = nn.Linear(2, 6)
        self.layer2 = nn.Linear(6, 2)


    def forward(self, x):
        x = torch.tanh(self.layer1(x))
        # x = torch.softmax(self.layer2(x), dim=1)
        x = self.layer2(x)
        return x


