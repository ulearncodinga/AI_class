def l2_reg(model, loss, par, regularizers_layer):
    l2_reg = 0
    # 计算 L1 正则化项
    for name, param in model.named_parameters():
        if par in name:
            if 'weight' in name:
                l2_reg += torch.norm(param, p=2)
    # 添加权重和偏置的正则化项到各层损失函数中
    loss += regularizers_layer * l2_reg
    return loss

