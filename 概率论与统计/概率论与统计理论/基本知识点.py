'''
概率:衡量事件发生可能性的指标

事件A的概率是 : P(A)
A的补集 : 1 - P(A)

互斥事件:
    不可能同时发生的事件

非互斥事件:
    两个事件可能同时发生
    P(A∪B) = P(A)+P(B)-P(A∩B)    

独立性事件:
    如果两个事件的发生互不影响,即一个事件的发生并不改变另一个事件发生的概率,那么这两个事件被称为是独立的
    (默认事件都会认为是独立事件)
    P(A∩B)=P(A)*P(B)


条件概率:(发生在非独立事件上)
    用于描述在已知某件事发生的情况下,另一件事发生的概率
    
    P(A∩B) = P(A) * P(B|A) 
    P(B|A) = P(A∩B) / P(A)
    P(A|B) = P(A∩B) / P(B) 
'''