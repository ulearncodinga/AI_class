"""
文件是一个存储在某种持久性存储介质（如硬盘、固态驱动器或磁带等）上的数据集合。
文件可以包含各种类型的信息，包括文本、图像、音频、视频、应用程序代码以及其他类型的二进制数据。
文件是操作系统用来组织和管理这些数据的主要方式之一。

文件有以下几部分组成:
数据:文件中存储的信息,如文本,图像,代码
元数据:关于文件本身的附加信息,如:文件名,创建日期,文件大小
文件系统::这是操作系统用来组织和刮玻璃文件的一直逻辑结构,包括文件的命名,存储和检索方式.
文件系统还负责管理磁盘空间分类,确保文件可以被正确的读写,常见的文件系统:FAT32,NTFS(Windows,打开文件管理器,右击磁盘,点击属性就能看到文件系统)
"""
"""
文件的属性:
文件名:用于标识文件的唯一名称,通常包含主文件名和拓展名
位置:文件存储在一个特定的位置,可以用目录或者路径来表示
文件大小:文件占用的存储空间的大小
创建日期和时间:文件被创建的时间戳
修改日期和时间:文件最后一次被修改的时间戳
访问权限:定义不同用户对文件的读,写,执行权限
"""
