#函数的返回值
def sub(x,y):
    z = x-y
    return z
    #return x - y
ret = sub(1,5)#要用变量接受返回值
print(ret)

def div(x,y):
    if y == 0:
        return '除数不能为0'
    else:
        return x / y

ret = div(1,0)
print(ret)


def calculate(x,y):
    z1 = x+y
    z2 = x-y
    z3 = x*y
    z4 = x/y
    return z1,z2,z3,z4
ret = calculate(5,10)#可以把ret换成z1,z2,z3,z4
print(ret)#如果上面换下面也要换

 