    def __init__(self):
        super(Model,self).__init__()
        self.layer1 = nn.Linear(1,6)
        self.layer2 = nn.Linear(6,1)

    def forward(self,x):
        x = torch.tanh(self.layer1(x))
        x = self.layer2(x)
        return x

