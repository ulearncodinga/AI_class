"""异常的概念
异常是一个事件，会在程序执行过程中产生，并且会影响程序的正常执行。
•
一般情况下，
Python遇见错误的代码或者无法正常处理程序就会产生一个异常并抛出。
•
异常被抛出后，可以被捕捉，捕捉后程序会按照某种机制继续运行，
如果对抛出的异常不做任何处理的话，那么程序就会终止运行。

常见的异常:
KeyboardInterrupt
用户主动结束程序触发
AttributeError
尝试访问对象没有的属性时触发
TypeError
操作非法类型的数据时触发
IndexError
序列中没有索引时触发
KeyError
映射（如字典）中没有键时触发
Exception
通用的异常类，用于捕获其他所有的异常情况
"""
